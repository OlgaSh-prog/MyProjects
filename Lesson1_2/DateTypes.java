package Lesson1_2;

public class DateTypes {

    public static void main(String[] args) {

        // Exercise 1 (Date Types)
        System.out.println("Exercise 1 (Date Types)\n");
        boolean a = true;       //boolean a = “true”;
        byte b = 127;          //byte b  = 129;
        short c = 25;           //short c = 25f;
        long e = 6456567774L;   //long e = 6456567774;
        int t = -1798954356;    //int t = -8798954356;
        double p = -6;          //double p = ‘-6’;
        float s = 4.89f;        //float s = 4.89;
        char l = 'y';            //char l = “y”;

        System.out.println("boolean a = " + a);
        System.out.println("byte b  = " + b);
        System.out.println("short c = " + c);
        System.out.println("long e = " + e);
        System.out.println("int t = " + t);
        System.out.println("double p = " + p);
        System.out.println("float s = " + s);
        System.out.println("char l = " + l);

        System.out.println("");

        //Exercise 2
        System.out.println("Exercise 2");
        for (int y = 0; y <= 10; y++) {
            System.out.print(y +" ");
        } System.out.println("\n");

        //Exercise 3
        System.out.println("Exercise 3");
        for (int z = 10; z >= 0; z--) {
            System.out.print(z +" ");
        } System.out.println("");

    }
}
